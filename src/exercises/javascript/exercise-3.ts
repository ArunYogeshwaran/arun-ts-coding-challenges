/**
 * Create a function that takes an object and returns
 * a new object where the original values become the keys
 * and the original keys become the values.
 * The input object will have unique values for each key.
 *
 * Note: Use the `Object.keys` method.
 *
 * Input: {id: 1, name: "Bob"}
 * Output: {1: "id", Bob: "name"}
 */
export const exercise3 = (
  input: Record<string, string | number>
): Record<string, string> => {
  const keys = Object.keys(input);
  console.log(keys);

  const result: Record<string, string> = { };
  for (const key of keys) {
    const value = input[key];
    result[value] = key;
  }
  
  return result;
};
