/**
 * Implement a function that sort a given string. Each word in the string will contain a single number.
 * This number is the position the word should have in the result.
 * If the input string is empty, return an empty string. The words in the input string will only contain valid consecutive numbers.
 *
 * Note: Numbers can be from 1 to 9. So 1 will be the first word (not 0).
 *
 * Input: "is2 Thi1s T4est 3a"
 * Output: "Thi1s is2 3a T4est"
 */
export const exercise5 = (input: string): string => {
    const words = input.split(" ");
    console.log(words);
    
    if (words[0] === '') { 
        return ""
    };
    const result: string[] = [];

    for (const word of words) {
        const index: number = parseInt(word.match(/\d/));
        result[index - 1] = word;
    }

    return result.join(" ").trim();
};
